			<p>
				Safer Spaces for Women and Girls is a creative intervention that explores how women and girls
				feel about safety in public places.
			</p>
			<p>
				The art based installation is made up of four physical 'sculptures' named 'Penny's'. The
				sculptures display digital and physical art works provided by local artists alongside
				information about community events and local information in Penrith. One of the 'Pennys' also
				contains a short survey questionnaire designed by the research team in response to a series of
				workshops on the 24th November and 4th of December 2023 about safety in public spaces. These
				workshops were led by NNN in a trauma informed and culturally safe space with women and girls
				in Penrith. We are looking for women and girls to give feedback on the initial content on the
				Penny's through a short anonymous survey. If you would like to complete the survey, approach
				the 'Penny' and follow the instructions which will enable you to give consent to the research
				team to use your data before scrolling through a series of questions. The data collected from
				the survey will be used to help understand what women and girls want and need to feel safer in
				public. It will also inform planning policy and procedures.
			</p>
