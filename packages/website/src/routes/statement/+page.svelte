				> or any successor Guideline, and applicable University of Newcastle policy provisions (as amended
				from time to time).
			</p>
			<h3>How will the information collected be used?</h3>
			<p>
				The survey answers will contribute towards the field of research of the creative industries
				and/or may be presented in academic publications, journals, conferences or to local
				communities through smart cities and regional smart cities engagement. It will also be used to
				influence future planning policy and engagement activities. Individual participants will not
				be identifiable in any reports arising from the project although individual anonymous
				responses may be quoted in publications and may feature on the Pennys display. The survey data
				will be encrypted and stored in a password protected online account managed by the research
				team who will conduct the analysis and share these results with stakeholders and interested
				parties as data visualizations on the Pennys themselves and via an online data dashboard. The
				data will be used for research publications, and will be analysed for user engagement feedback
				and can be incorporated into these and any future designs.
			</p>
