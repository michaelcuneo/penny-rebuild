			<h3>Why is the research being done?</h3>
			<p>
				The project aims to raise awareness of the issue of safety for women and girls, and to inspire
				action from the community and the authorities to make public places more inclusive and
				welcoming for everyone. Many women don't feel safe when they are out and about, especially at
				night. A study by Transport for NSW (2023) asked women and girls how they felt about safety in
				public places in Sydney. It found that more than half of them were scared after dark, and
				almost all of them thought about safety when they decided where to go and how to get there.
				Some researchers have said that public places should be designed with women's safety in mind
				but this has not been done much in reality. To make public places safer for women and girls,
				we need to know more about how they use them and what makes them feel safe or unsafe. That is
				the goal of this research.
			</p>
			<h3>Who can participate in the research?</h3>
			<p>
				The research is open to anyone but we are particularly interested in those who identify as a
				woman or a girl, regardless of their, background, or ability. The research is also interested
				in hearing from people who have different experiences of public spaces, such as people who
				live in different areas, people who have disabilities, people who belong to minority groups,
				or people who have faced harassment or violence in public. The research is voluntary and
				confidential, and participants can choose whether they want to complete the short survey
				displayed on screen. Before agreeing to take part, please take as much time as you require to
				consider your involvement by reviewing the contents of this Information Statement which you
				have accessed through the QR code on the 'Pennys' screen display.
			</p>
			<h3>What would you be asked to do?</h3>
			<p>
				You can participate in the research by visiting the 'Penny's' installations located in Penrith
				City Park. On one the 'Pennys' you will see a screen that invites you to participate in the
				survey. The survey asks a series of questions about safety in public places. You can answer as
				many questions as you like, and you can stop at any time. The survey should take no more than
				7 minutes. Your answers will be stored anonymously and securely, and they will be used for
				research purposes only.
			</p>
			<h3>What choice do you have?</h3>
			<p>
				Participation in this research is entirely your choice. You may withdraw from the project at
				any time simply by not completing the survey. Whether or not you decide to participate, your
				decision will not disadvantage you. If you do not wish to participate at all, simply do not
				respond to the on screen invitation to complete the survey.
			</p>
			<h3>How much time will it take?</h3>
			<p>
				You can choose the amount of time you wish to spend completing the survey moving through each
				question by following the instructions on 'Penny'. However, the screen has been pre-programmed
				to return to its standby position after three minutes of inactivity. If you wished to answer
				the full cycle of questions it will take you around 7 minutes.
			</p>
			<h3>What are the risks and benefits of participating?</h3>
			<p>
				The risks of participating in the research are minimal. You might feel uncomfortable or
				emotional when talking about safety issues, or you might encounter some technical problems
				when interacting with the 'Pennys'. You can choose not to answer any questions that make you
				feel uncomfortable, and you can stop participating at any time. You can also contact the
				researchers if you have any questions or concerns, or if you need any support or assistance.
				You can also contact Life Line Australia by calling <a href="tel: 13 11 14">13 11 14</a> or by
				SMS text on
				<a href="tel: 0477 13 11 14">0477 13 11 14</a> The benefits of participating in the research are
				that you can contribute to a better understanding of how women and girls feel about safety in public
				places, and what can be done to improve it.
			</p>
			<h3>How will your privacy be protected?</h3>
			<p>
				You do not need to provide any personal information to participate in the research, such as
				your name, email, phone number, or address. Your answers to the questions will be stored
				anonymously and securely on a cloud server, and they will be encrypted and password-protected.
				Your answers will only be accessed by the researchers authorised parties, and they will only
				be used for research purposes. Your answers will not be shared with anyone else without your
				consent, unless required by law or ethics. Data will be retained securely for a minimum period
				of five (5) years from completion of the research and managed/stored in accordance with the
				University's Research Data and Materials Management Guideline. See
				<a href="https://policies.newcastle.edu.au/document/view-current.php?id=72"
